package br.com.onesystem.war.converter;

import br.com.onesystem.domain.Cotacao;
import br.com.onesystem.war.service.CotacaoService;
import java.util.List;
import javax.faces.application.FacesMessage;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.ConverterException;
import javax.faces.convert.FacesConverter;

/**
 *
 * @author Rafael
 */
@FacesConverter(value = "cotacaoConverter", forClass = Cotacao.class)
public class CotacaoConverter implements Converter {

    @Override
    public Object getAsObject(FacesContext fc, UIComponent uic, String value) {
        if (value != null && value.trim().length() > 0) {
            try {
                CotacaoService service = new CotacaoService();
                List<Cotacao> lista = service.buscarCotacoes();
                for (Cotacao conta : lista) {
                    if (conta.getId().equals(new Long(value))) {
                        return conta;
                    }
                }
                return null;
            } catch (NumberFormatException e) {
                throw new ConverterException(new FacesMessage(FacesMessage.SEVERITY_ERROR, "Conversion Error", "Not a valid theme."));
            }
        } else {
            return null;
        }
    }

    @Override
    public String getAsString(FacesContext fc, UIComponent uic, Object object) {
        if (object != null) {
            return String.valueOf(((Cotacao) object).getId());
        } else {
            return null;
        }
    }
}
